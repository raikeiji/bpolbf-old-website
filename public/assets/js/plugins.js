let scrollpos = window.scrollY
const nav = document.querySelector(".home-nav")
const nav_height = nav.offsetHeight

const add_class_on_scroll = () => nav.classList.add("home-nav-transparent")
const remove_class_on_scroll = () => nav.classList.remove("home-nav-transparent")

function onScroll(){
	scrollpos = window.scrollY;

  if (scrollpos >= nav_height) { remove_class_on_scroll() }
  else { add_class_on_scroll() }
}
window.addEventListener("scroll", onScroll);